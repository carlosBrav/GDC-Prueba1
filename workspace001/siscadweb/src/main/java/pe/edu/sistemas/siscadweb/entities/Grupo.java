package pe.edu.sistemas.siscadweb.entities;

public class Grupo {
	private int idGrupo;
	private int grupoNumero;
	
	public Grupo(){
		super();
	}

	public Grupo(int idGrupo, int grupoNumero) {
		super();
		this.idGrupo = idGrupo;
		this.grupoNumero = grupoNumero;
	}

	public int getIdGrupo() {
		return idGrupo;
	}

	public void setIdGrupo(int idGrupo) {
		this.idGrupo = idGrupo;
	}

	public int getGrupoNumero() {
		return grupoNumero;
	}

	public void setGrupoNumero(int grupoNumero) {
		this.grupoNumero = grupoNumero;
	}
	
}
