package pe.edu.sistemas.siscadweb.entities;

public class Motivo {
	private int idMotivo;
	private String motivoNombre;
	private int motivoTipo;
	
	public int getIdMotivo() {
		return idMotivo;
	}
	public void setIdMotivo(int idMotivo) {
		this.idMotivo = idMotivo;
	}
	public String getMotivoNombre() {
		return motivoNombre;
	}
	public void setMotivoNombre(String motivoNombre) {
		this.motivoNombre = motivoNombre;
	}
	public int getMotivoTipo() {
		return motivoTipo;
	}
	public void setMotivoTipo(int motivoTipo) {
		this.motivoTipo = motivoTipo;
	}
}
