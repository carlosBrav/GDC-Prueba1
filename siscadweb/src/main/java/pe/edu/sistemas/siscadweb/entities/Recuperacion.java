package pe.edu.sistemas.siscadweb.entities;

import java.sql.Date;
import java.sql.Time;

public class Recuperacion {

	private Integer idRecuperacion;
	private int idHorarioClase;
	private int idmotivo;
	private Date fechaRecuperacion;
	private Date fechaAusente;
	private String aula;
	private Time horaInicio;
	private Time horaFin;
	private String observacion;
	private Time horaInicioReal;
	private Time horaFinReal;
	private int estado;

	public Integer getIdRecuperacion() {
		return idRecuperacion;
	}

	public void setIdRecuperacion(Integer idRecuperacion) {
		this.idRecuperacion = idRecuperacion;
	}

	public int getIdHorarioClase() {
		return idHorarioClase;
	}

	public void setIdHorarioClase(int idHorarioClase) {
		this.idHorarioClase = idHorarioClase;
	}

	public int getIdmotivo() {
		return idmotivo;
	}

	public void setIdmotivo(int idmotivo) {
		this.idmotivo = idmotivo;
	}

	public Date getFechaRecuperacion() {
		return fechaRecuperacion;
	}

	public void setFechaRecuperacion(Date fechaRecuperacion) {
		this.fechaRecuperacion = fechaRecuperacion;
	}

	public Date getFechaAusente() {
		return fechaAusente;
	}

	public void setFechaAusente(Date fechaAusente) {
		this.fechaAusente = fechaAusente;
	}

	public String getAula() {
		return aula;
	}

	public void setAula(String aula) {
		this.aula = aula;
	}

	public Time getHoraInicio() {
		return horaInicio;
	}

	public void setHoraInicio(Time horaInicio) {
		this.horaInicio = horaInicio;
	}

	public Time getHoraFin() {
		return horaFin;
	}

	public void setHoraFin(Time horaFin) {
		this.horaFin = horaFin;
	}

	public String getObservacion() {
		return observacion;
	}

	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}

	public Time getHoraInicioReal() {
		return horaInicioReal;
	}

	public void setHoraInicioReal(Time horaInicioReal) {
		this.horaInicioReal = horaInicioReal;
	}

	public Time getHoraFinReal() {
		return horaFinReal;
	}

	public void setHoraFinReal(Time horaFinReal) {
		this.horaFinReal = horaFinReal;
	}

	public int getEstado() {
		return estado;
	}

	public void setEstado(int estado) {
		this.estado = estado;
	}

}
